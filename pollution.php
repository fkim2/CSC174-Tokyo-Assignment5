	<h2>Types of Pollution</h2>
    <img src = "images/numbers.png" alt = "pollution statistics">
	
	<h3>Oil</h3>
	<p>Oil spills cause huge damage to the marine environment - but in fact are responsible for only around 12% of the oil entering the seas each year. According to a study by the US National Research Council, 36% comes down drains and rivers as waste and runoff from cities and industry. </p>

	<h3>Fertilizers</h3>
	<p>Fertilizer runoff from farms and lawns is a huge problem for coastal areas. The extra nutrients cause eutrophication - flourishing of algal blooms that deplete the water's dissolved oxygen and suffocate other marine life.</p>

	<h3>Garbage</h3>
	<p>Solid garbage also makes its way to the ocean. Plastic bags, balloons, glass bottles, shoes, packaging material – if not disposed of correctly, almost everything we throw away can reach the sea.</p>
	
	<h3>Sewage disposal</h3>
	<p>In many parts of the world, sewage flows untreated, or under-treated, into the ocean. For example, 80% of urban sewage discharged into the Mediterranean Sea is untreated.</p>

	<h3>Toxic chemicals</h3>
	<p>Almost every marine organism, from the tiniest plankton to whales and polar bears, is contaminated with man-made chemicals, such as pesticides and chemicals used in common consumer products.Some of these chemicals enter the sea through deliberate dumping. For centuries, the oceans have been a convenient dumping ground for waste generated on land. This continued until the 1970s, with dumping at sea the accepted practise for disposal of nearly everything, including toxic material such as pesticides, chemical weapons, and radioactive waste.</p>

